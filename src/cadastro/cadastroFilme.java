    private void btnContActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContActionPerformed
        // TODO add your handling code here:
        //Pegando os dados inseridos no painel anterior
        try{
            String nomeDB = confirNome.getText();
            String dataDB = confirData.getText();
            String categoriaDB = confirCategoria.getText();
            
            //Convertendo a string do campo data para DATE 
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
            java.util.Date dataUtil = sdf.parse(dataDB);
            java.sql.Date dataSQL = new java.sql.Date(dataUtil.getTime());
            
            //Comando pata inserção de dados no MySQL
            st.setString(1,nomeDB);
            st.setDate(2,dataSQL);
            st.setString(3,categoriaDB);
            
            //Executando a inserção
            int resultado = st.executeUpdate();
            if (resultado > 0){
                //Se deu certo, esconde os paineis indesejáveis
                telaCadastro.setVisible(false);
                jConfirmacao.setVisible(false);
                jCadastrado.setVisible(true);
                jResultado.setVisible(false);
                jInicio.setVisible(false);
                jBusca.setVisible(false);
                jEditar.setVisible(false);
            }
        }catch(ParseException ex){
            JOptionPane.showMessageDialog(this,"Erro formato Data"+ex.getMessage(),"Erro",JOptionPane.ERROR_MESSAGE);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this,"Erro ao inserir o banco"+ex.getMessage(),"Erro",JOptionPane.ERROR_MESSAGE);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,"Algo deu errado"+ex.getMessage(),"Erro",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnContActionPerformed

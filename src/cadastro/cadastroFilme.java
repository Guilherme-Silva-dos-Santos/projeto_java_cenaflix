    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        // TODO add your handling code here:
        //Mostrando erro se caso o campo de busca estier vazio
        if (jCampoBusca.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Por favor, preencha o campo de busca");
            return; //Estando vazio não continua a busca
        }
        //Especificando o que o combo Box irá ser atribuído
        String colunaSelecionada = jEscolha.getSelectedItem().toString();
        String colunaBanco = "";
        
        //Atribui a coluna conforme o Combo Box
        switch (colunaSelecionada) {
            case "Nome":
                colunaBanco = "nome";
                break;
            case "Categoria":
                    colunaBanco = "categoria";
                    break;
        }
        //Recupera o texto digitado no campo da busca
        String termoBusca = jCampoBusca.getText().trim();
        
        //Criando um modelo para tabela
        DefaultTableModel modelo = (DefaultTableModel) jTabelaResult.getModel();
        
        //Limpa resultados antigos
        modelo.setRowCount(0);
        
        //Definindo a consulta no SQL
        String sql = "SELECT id,nome,datalancamento, categoria FROM filmes WHERE "+colunaBanco+" LIKE ?";
    
        //Usando a conexão existente 
        try {
            st = conexao.prepareStatement(sql);
            st.setString(1,"%" + termoBusca + "%");
            
            //executa a consulta
            ResultSet rs = st.executeQuery();
            
            //Preenche e tabela com os resultados da consulta
            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nome"),
                    rs.getString("datalancamento"),
                    rs.getString("categoria"),    
                });    
            }
            //Ocultando o id
            ocultarColunaID();
            
            telaCadastro.setVisible(false);
            jConfirmacao.setVisible(false);
            jCadastrado.setVisible(false);
            jInicio.setVisible(false);
            jBusca.setVisible(false);
            jResultado.setVisible(true);
            jEditar.setVisible(false);
            
        }catch (SQLSyntaxErrorException ex){
            //captura os erros de sintaze na query
            JOptionPane.showMessageDialog(null,"Erro Sintaxe SQL:\n"+ ex.getMessage());
            ex.printStackTrace();//Mostra o erro completo do console
        }catch (SQLNonTransientConnectionException ex) {
            //Capaturando erros da conexão do banco de dados
            JOptionPane.showMessageDialog(null, "Erro de conexão do banco de dados:\n"+ ex.getMessage());
            ex.printStackTrace();
        }catch(SQLException ex){
            //Captura qualquer outro erro relacionado ao banco de dados
            JOptionPane.showMessageDialog(null,
                    "Erro ao acessar o banco de dados:\n"+ ex.getMessage()+
                    "\nCódigo do erro: "+ ex.getErrorCode()+
                    "SQLState: "+ ex.getSQLState());
            ex.printStackTrace();
        }catch(Exception ex){
            //Capturando erros genéricos do que não seja do SQL
            JOptionPane.showMessageDialog(null, "Erro inesperado:\n"+ ex.getMessage());
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnBuscaActionPerformed


    private void atualizarTabela() throws SQLException{
        try{
            //Limpa os dados antigos
            DefaultTableModel modelo = (DefaultTableModel) jTabelaResult.getModel();
            modelo.setRowCount(0);
            
            //Consulta geral das colunas
            String sql = "SELECT * FROM filmes";
            st = conexao.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            
            //Preenchendo a tabela com dados em branco
            while (rs.next()){
                modelo.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nome"),
                    rs.getString("datalancamento"),
                    rs.getString("categoria")
                });
            }
            
            //Ocultando o ID da tabela 
            ocultarColunaID();
            
            //Fechando as conexões
            rs.close();
            st.close();
            
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Erro ao atualizar a tabela");
        }
    }
    
    private void btnDeletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletActionPerformed
        // TODO add your handling code here:
        //Obtendo Ìndice da linha selecionada
        int linhaSelecionada = jTabelaResult.getSelectedRow();
        
        //Verificando se o usuário selecionou alguma linha
        if (linhaSelecionada == -1){
            JOptionPane.showMessageDialog(null, "Selecione um filme para deletar.");
            return;
        }
        //Pegando o ID invisível da coluna
        int id = Integer.parseInt(jTabelaResult.getValueAt(linhaSelecionada, 0).toString());
        
        //Confirmando com o usuário se deseja realmente deletar os dados
        int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja DELETAR?","Confirmação",JOptionPane.YES_NO_OPTION);
        
        //Usuário clicando em sim
        if (confirmacao == JOptionPane.YES_OPTION){
            try{
                //comando sql para deletar os dados
                String sql = "DELETE FROM filmes WHERE id = ?";
                st = conexao.prepareStatement(sql);
                st.setInt(1, id);
                
                //Executando o comando
                int linhasAfetadas = st.executeUpdate();
                
                //Verificando se deletou com sucesso
                if (linhasAfetadas > 0) {
                    JOptionPane.showMessageDialog(null, "Filme deletado com sucesso.");
                    
                    //Atualizando a tabela depois da exclusão
                    atualizarTabela();
                    telaCadastro.setVisible(false);
                    jConfirmacao.setVisible(false);
                    jCadastrado.setVisible(false);
                    jInicio.setVisible(true);
                    jBusca.setVisible(false);
                    jResultado.setVisible(false);
                    jEditar.setVisible(false);
                }else{
                    JOptionPane.showMessageDialog(null,"Erro ao deletar filme.");
                }
                
                //Fechando conexão
                st.close();
                
            }catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro de SQL");
            } 
        }
    }//GEN-LAST:event_btnDeletActionPerformed

    private void btnAtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtActionPerformed
        // TODO add your handling code here:
       try{
            //Recuperando dados dos campos editados
            String nome = jNomeAt.getText().trim();
            String dataStr = jDataAt.getText().trim();
            String categoria = jCatAt.getText().trim();
        
        //Verificando se algum campo está em branco
            if (nome.isEmpty()|| dataStr.isEmpty()|| categoria.isEmpty()){
                JOptionPane.showMessageDialog(null,"Por favor, preencha todos os campos.");
                return;
            }
            //Convertendo a data para o formato do banco
            DateTimeFormatter formatoEntrada = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            DateTimeFormatter formatoSaida = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            LocalDate dataConvertida = LocalDate.parse(dataStr, formatoEntrada);
            String dataSQL = dataConvertida.format(formatoSaida);
        
            //Recuperando ID da linha selecionada na tabela
            int linhaSelecionada = jTabelaResult.getSelectedRow();
            if (linhaSelecionada == -1){
                JOptionPane.showMessageDialog(null, "Selecione um filme para atualizar.");
                return;
            }
            int id = Integer.parseInt(jTabelaResult.getValueAt(linhaSelecionada, 0).toString());
        
            //Preparando o comando SQL
            String sql = "UPDATE filmes SET nome = ?, datalancamento = ?, categoria=? WHERE id = ?";
            st = conexao.prepareStatement(sql);
            st.setString(1, nome);
            st.setString(2, dataSQL);
            st.setString(3, categoria);
            st.setInt(4, id);
        
            //Executa a atualização
            int linhasAfetadas = st.executeUpdate();
            if (linhasAfetadas > 0){
                JOptionPane.showMessageDialog(null, "Filme atualizado com sucesso!");
                atualizarTabela();
                telaCadastro.setVisible(false);
                jConfirmacao.setVisible(false);
                jCadastrado.setVisible(false);
                jInicio.setVisible(true);
                jBusca.setVisible(false);
                jResultado.setVisible(false);
                jEditar.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null, "Nenhum dado atualizado.");
                telaCadastro.setVisible(false);
                jConfirmacao.setVisible(false);
                jCadastrado.setVisible(false);
                jInicio.setVisible(true);
                jBusca.setVisible(false);
                jResultado.setVisible(false);
                jEditar.setVisible(false);
            }
            
       }catch (SQLException ex){
           JOptionPane.showMessageDialog(null, "Erro ao atualizar o Filme:\n"+ex.getMessage());
           ex.printStackTrace();
       }catch(Exception ex){
           JOptionPane.showMessageDialog(null, "ErroInesperado"+ex.getMessage());
           ex.printStackTrace();
       }
    }//GEN-LAST:event_btnAtActionPerformed
              
